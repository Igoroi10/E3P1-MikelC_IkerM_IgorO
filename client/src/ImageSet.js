export default class ImageSet{

    constructor(initFil, initCol, xSize, ySize, gridSize, xOffset, yOffset){
        this.initFil    = initFil;  //Fila inicio en el tileset
        this.initCol    = initCol;  //Columna inicio del tileset
        this.xSize      = xSize;    //tamaño de piexeles eje x
        this.ySize      = ySize;    //tamaño de piexeles eje y
        this.xOffset    = xOffset;  //Offset en X de comienzo de dibujo respecto al grid
        this.yOffset    = yOffset;  //Offset en Y de comienzo de dibujo respecto al grid
        this.gridSize   = gridSize; //tamaño del grid (el objeto no tiene porque llenar todo el grid por lo tanto será el tamaño del espacio)
    }
}